SELECT P.PROD_ID,
       P.PROD_NAME,
       P.PROD_CATEGORY
FROM SH.PRODUCTS P
LEFT JOIN SH.SALES S
ON P.PROD_ID = S.PROD_ID
WHERE S.PROD_ID ;


