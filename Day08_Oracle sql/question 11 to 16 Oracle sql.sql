SELECT C.CUST_ID,
       C.CUST_FIRST_NAME,
       C.CUST_LAST_NAME,
       C.CUST_CITY,
       C.COUNTRY_ID
FROM SH.CUSTOMERS C
LEFT JOIN SH.SALES S
ON C.CUST_ID = S.CUST_ID
WHERE S.CUST_ID IS NULL;

