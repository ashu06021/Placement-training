# Question:
# Demonstrate the use of built-in modules in Python.
#
# Concept:
# Modules are pre-written Python files.
# We use import keyword to access them.

import math

print("Square Root of 25 =", math.sqrt(25))

print("2 Power 5 =", math.pow(2, 5))

print("Value of Pi =", math.pi)

print("Ceil Value =", math.ceil(4.2))

print("Floor Value =", math.floor(4.8))